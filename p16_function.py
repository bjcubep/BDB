# 함수 overloading     
#     다른언어 - 파라메터가 다르면 함수명 같아도 됨 -> 호출할 때 구별이 되기 때문에
#     파이썬은 함수 overloading 지원 안됨 (기본값, 순서조절 등의 기능 때문에)
#        -> 함수명이 다 달라야 함

# 숫자를 2개 넣으면 사칙연산 결과를 출력하는 함수
def calculate(a=100, b=200):    # 함수에서 변수에 기본값 셋팅(파이썬에만 있음)
    """
        함수 사용법 설명
        내용을 적어요
        어쩌구 저쩌구
        help() 를 사용해서 볼 수 있음
    """
    print(a, b)
    print(a + b)
    print(a - b)
    print(a * b)
    print(a / b)
    print("-------------")

    
# 숫자 3개 넣으면 사칙연산 ...
def calculate2(a, b, c):
    print(a+b+c)
    

