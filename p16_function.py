calculate(5, 2)  # 영역이 잡혔을 경우 tab키를 누르면 다음 영역으로 이동
calculate(b = 30, a = 10)  # 변수명과 값을 적어주면 순서 조절 가능
calculate(b=50)     # a는 기본값 100  b는 50으로
calculate(30)       # a는 30, b는 기본값 200

